int		ft_is_prime(int n)
{
	int	i;
	int	w;

	if (n == 2 || n == 3)
		return (1);
	if (n < 2 || n % 2 == 0 || n % 3 == 0)
		return (0);
	i = 5;
	w = 2;
	while ((i * i) <= n)
	{
		if (n % i == 0)
			return (0);
		i += w;
		w = 6 - w;
	}
	return (1);
}
