#include "libft.h"

int		ft_padicval(int nb, int p)
{
	int	res;

	if (ft_is_prime(p) == 0 || nb <= 0)
		return (-1);
	res = 0;
	while (nb % p == 0)
	{
		nb /= p;
		res++;
	}
	return (res);
}
